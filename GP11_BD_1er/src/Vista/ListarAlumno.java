    private void listarAlumnosPorMaterias(int idMateria) {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID Materia");
        modelo.addColumn("Materia");
        modelo.addColumn("ID Alumno");
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido");

        Tabla1.setModel(modelo);

        String sql = "SELECT m.idMateria, m.nombre AS nombreMateria, a.idAlumno, a.nombre, a.apellido "
                + "FROM inscripcion i "
                + "JOIN alumno a ON i.idAlumno = a.idAlumno "
                + "JOIN materia m ON i.idMateria = m.idMateria "
                + "WHERE m.idMateria = ?";

        try (Connection con = Conexion.getConexion(); PreparedStatement ps = con.prepareStatement(sql)) {

            ps.setInt(1, idMateria);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                Object[] fila = {
                    rs.getInt("idMateria"),
                    rs.getString("nombreMateria"),
                    rs.getInt("idAlumno"),
                    rs.getString("nombre"),
                    rs.getString("apellido")
                };
                modelo.addRow(fila);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al listar alumnos: " + e.getMessage());
        }
    }
